BEGIN
  -- 기존 언더커버 게시글 전체 삭제
  DELETE FROM public.post_comments WHERE post_id IN (SELECT id FROM public.posts WHERE board = 'brand');
  DELETE FROM public.post_likes WHERE post_id IN (SELECT id FROM public.posts WHERE board = 'brand');
  DELETE FROM public.posts WHERE board = 'brand';

  FOR brand_rec IN SELECT id FROM public.brands LOOP
    FOR flair_idx IN 1..8 LOOP
      cur_flair := flairs[flair_idx];
      CASE cur_flair
        WHEN '자유'        THEN cur_posts := p_jayu;    cur_comments := c_jayu;
        WHEN '수익·매출'   THEN cur_posts := p_maeul;   cur_comments := c_maeul;
        WHEN '본사 이야기' THEN cur_posts := p_bonsa;   cur_comments := c_bonsa;
        WHEN '직원 구인'   THEN cur_posts := p_jikwon;  cur_comments := c_jikwon;
        WHEN '메뉴·식재료' THEN cur_posts := p_menu;    cur_comments := c_menu;
        WHEN '장비·시설'   THEN cur_posts := p_jangbi;  cur_comments := c_jangbi;
        WHEN '행사·이벤트' THEN cur_posts := p_haengsa; cur_comments := c_haengsa;
        WHEN '질문'        THEN cur_posts := p_jilmun;  cur_comments := c_jilmun;
      END CASE;

      FOR j IN 1..10 LOOP
        SELECT
          p.id,
          CASE WHEN COALESCE(p.region,'') != '' THEN p.nickname||'_'||p.region ELSE p.nickname END AS nick
        INTO author_rec
        FROM public.profiles p
        WHERE p.brand_id = brand_rec.id AND p.email LIKE '%@fcindex.dummy'
        ORDER BY random() LIMIT 1;

        CONTINUE WHEN author_rec.id IS NULL;

        p_title   := split_part(cur_posts[j], '|||', 1);
        p_content := split_part(cur_posts[j], '|||', 2);
        num_comments := floor(random() * 3 + 5)::int; -- 5~7개

        INSERT INTO public.posts
          (author_id, brand_id, nickname, title, content, board, flair, is_anonymous, like_count, comment_count, created_at, is_dummy)
        VALUES
          (author_rec.id, brand_rec.id, author_rec.nick, p_title, p_content, 'brand', cur_flair, true,
           floor(random()*70+3)::int, num_comments,
           now() - (random() * interval '60 days'), true)
        RETURNING id INTO v_post_id;

        -- 해당 게시글 전용 댓글 삽입: (j-1)*7+k 인덱스
        FOR k IN 1..num_comments LOOP
          SELECT
            p.id,
            CASE WHEN COALESCE(p.region,'') != '' THEN p.nickname||'_'||p.region ELSE p.nickname END AS nick
          INTO commenter_rec
          FROM public.profiles p
          WHERE p.brand_id = brand_rec.id AND p.email LIKE '%@fcindex.dummy'
          ORDER BY random() LIMIT 1;

          CONTINUE WHEN commenter_rec.id IS NULL;

          INSERT INTO public.post_comments
            (post_id, author_id, content, nickname, created_at, is_dummy)
          VALUES
            (v_post_id, commenter_rec.id,
             cur_comments[(j-1)*7 + k],
             commenter_rec.nick,
             now() - (random() * interval '50 days'),
             true);
        END LOOP;
      END LOOP;
    END LOOP;
  END LOOP;
END $$;
